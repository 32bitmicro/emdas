#!/usr/bin/env python3

import sys
import struct

sets = {
    'ill.bin' : [0b0000000000000000, 0b0100000000000000],
    'na1.bin' : [0b0100000000000000, 0b0111110000000000],
    'fd.bin'  : [0b0111110000000000, 0b1000000000000000],
    'na2.bin' : [0b1000000000000000, 0b1100000000000000],
    'ka1.bin' : [0b1100000000000000, 0b1110000000000000],
    'js.bin'  : [0b1110000000000000, 0b1110010000000000],
    'ka2.bin' : [0b1110010000000000, 0b1110100000000000],
    'c.bin'   : [0b1110100000000000, 0b1110110000000000],
    's.bin'   : [0b1110110000000000, 0b1111000000000000],
    'j.bin'   : [0b1111000000000000, 0b1111010000000000],
    'l.bin'   : [0b1111010000000000, 0b1111100000000000],
    'g.bin'   : [0b1111100000000000, 0b1111110000000000],
    'bn.bin'  : [0b1111110000000000, 0b1111111111111111],
    'all.bin' : [0b0000000000000000, 0b1111111111111111]
}

# ------------------------------------------------------------------------
def usage(s):
    print(s)
    print("Usage: %s <set>" % sys.argv[0])
    print("Known sets: ", end="")
    for i in sets.keys():
        print(i, " ", end="")
    print()

# ------------------------------------------------------------------------
# ---- MAIN --------------------------------------------------------------
# ------------------------------------------------------------------------

try:
    out = sys.argv[1]
except:
    usage("Missing argument")
    sys.exit(1)

try:
    op_start = sets[out][0]
    op_stop = sets[out][1]
except:
    usage("Unknown set: %s" % out)
    sys.exit(1)

f = open(out, "wb")
for i in range(op_start, op_stop):
    f.write(struct.pack(">H", i))
f.close();

